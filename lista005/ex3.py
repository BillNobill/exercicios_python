num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))

print("A soma entre os números é " + str(int(num1) + int(num2)))
print("O produto entre eles é " + str(int(num1) * int(num2)))
print("O quociente é " + str(int(num1) / int(num2)))
print("O resto da divisão é " + str(int(num1) % int(num2)))