print("|-------------Diferença entre Quadrados e Cubos-------------|")
# Quadrado
x = int(input("Digite um número inteiro: "))
# Cubo
y = int(input("Digite outro número inteiro: "))

# Diferença (D)
d = (x ** 2) - (y ** 3)

print("")
print(f"A diferença entre o quadrado do primeiro número e o cubo do segundo número é: {d}")
